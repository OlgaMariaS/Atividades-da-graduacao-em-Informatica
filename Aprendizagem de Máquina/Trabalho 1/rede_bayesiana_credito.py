import matplotlib.pyplot as plt
import numpy as np

fig, axes = plt.subplots(1, 3, figsize=(15, 5))
fig.suptitle('Inferências Probabilísticas — Credit_Score', fontsize=14, fontweight='bold')

consultas = [
    (resultado1, 'Perfil de Risco Alto\n(Jovem, Renda Baixa, Atrasos Graves)'),
    (resultado2, 'Perfil de Baixo Risco\n(Adulto, Renda Alta, Sem Atrasos)'),
    (resultado5, 'Credit_Mix=Good\nDívida Baixa'),
]

cores_score = {'Good': '#01696f', 'Standard': '#4f98a3', 'Poor': '#a12c7b'}

for ax, (res, titulo) in zip(axes, consultas):
    estados = res.state_names['Credit_Score']
    valores = [res.values[res.state_names['Credit_Score'].index(s)] for s in estados]
    cores = [cores_score.get(s, '#888') for s in estados]
    bars = ax.bar(estados, valores, color=cores, edgecolor='white', linewidth=1.5)
    for bar, val in zip(bars, valores):
        ax.text(bar.get_x() + bar.get_width()/2, bar.get_height() + 0.01,
                f'{val:.2%}', ha='center', va='bottom', fontsize=10, fontweight='bold')
    ax.set_ylim(0, 1)
    ax.set_ylabel('Probabilidade')
    ax.set_title(titulo, fontsize=10)
    ax.yaxis.set_major_formatter(plt.FuncFormatter(lambda y, _: f'{y:.0%}'))
    ax.spines['top'].set_visible(False)
    ax.spines['right'].set_visible(False)

plt.tight_layout()
plt.savefig('inferencias_credit_score.png', dpi=150, bbox_inches='tight')
plt.show()
print("Gráfico salvo como 'inferencias_credit_score.png'")

